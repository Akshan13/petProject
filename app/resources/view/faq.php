    <div class="faq-item">
        <h3 class="question">How does the pet adoption process work?</h3>
        <div class="answer">
            <p>The pet adoption process typically involves several steps, including browsing available pets on our website, contacting the pet's owner or adoption agency, filling out an adoption application, scheduling a meet-and-greet with the pet, and completing the adoption paperwork if the match is successful.</p>
        </div>
    </div>

    <div class="faq-item">
        <h3 class="question">Are the pets on your website vaccinated and spayed/neutered?</h3>
        <div class="answer">
            <p>We encourage pet owners to ensure that their pets are up-to-date on vaccinations and spayed/neutered before listing them for adoption. However, it's essential to inquire about the pet's medical history and current health status directly with the pet's owner or adoption agency.</p>
        </div>
